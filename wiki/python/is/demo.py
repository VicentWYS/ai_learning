"""
Python `is` 运算符 — 快速回忆指南
===================================
`is` 比较的是内存地址（身份标识），不是值。
`id(a) == id(b)` 等价于 `a is b`。

核心记忆口诀：
  is   → 判断『是不是同一个东西』（身份）
  ==   → 判断『长得一不一样』（值）
  is not → 不是同一个东西
"""

# ============================================================
# 1. is vs == —— 最核心的区别
# ============================================================
a = [1, 2, 3]
b = [1, 2, 3]       # b 是另一个列表，值相同但对象不同
c = a               # c 和 a 指向同一个列表对象

print(a == b)       # True  —— 值相等
print(a is b)       # False —— 不是同一个对象（两块内存）
print(a is c)       # True  —— 同一个对象

# ============================================================
# 2. None 必须用 is 判断（唯一正确的写法）
# ============================================================
x = None
if x is None:       # ✅ 正确：None 是单例，按身份判断最快最安全
    ...
# if x == None:     # ❌ 千万别这样写！== 可能被自定义 __eq__ 覆盖，导致误判
# if x is not None: # ✅ 判断"不是 None"的正确写法

# ============================================================
# 3. 小整数缓存 [-5, 256] —— 常见面试陷阱
# ============================================================
a = 256
b = 256
print(a is b)       # True  —— 256 在缓存范围内，复用同一个对象

a = 257
b = 257
print(a is b)       # False —— 超出缓存范围，每次创建新对象

# ⚠️ 永远不要用 is 比较数字的值！用 ==
# print(1000 is 1000)  # 可能 True 也可能 False，看解释器实现

# ============================================================
# 4. 字符串驻留（interning）—— 编译期自动驻留
# ============================================================
a = "hello"
b = "hello"
print(a is b)       # True —— 相同字面量，编译时被驻留成同一个对象

a = "hello world!"  # 含空格等特殊字符的较长字符串
b = "hello world!"
print(a is b)       # 通常是 True（但不要依赖它，用 == ）

# ============================================================
# 5. is not —— 不是同一个对象
# ============================================================
a = [1, 2]
b = [1, 2]
print(a is not b)   # True —— a 和 b 是不同的列表对象

# ============================================================
# 6. 类型判断 —— 用 is 比较类/类型
# ============================================================
x = 42
print(type(x) is int)       # True  —— 精确匹配 int
print(isinstance(x, int))   # True  —— 推荐：能处理子类继承

# ============================================================
# 7. 判断 True / False（它们是单例）
# ============================================================
# True 和 False 在 Python 中也是单例，用 is 判断即可
flag = True
if flag is True:            # 可以，但通常直接写 if flag:
    ...

# ============================================================
# 8. 可变对象永远不会 is 相等（除非赋值同一个引用）
# ============================================================
a = {}
b = {}
print(a is b)       # False —— 空字典，每次创建都是新对象

a = []
b = []
print(a is b)       # False —— 空列表同理

a = ()
b = ()
print(a is b)       # True —— 空元组！元组是不可变对象，解释器复用同一个

# ============================================================
# 9. 单例模式 —— is 的经典应用场景
# ============================================================
class Singleton:
    _instance = None

    def __new__(cls):
        if cls._instance is None:          # 用 is 检查是否已有实例
            cls._instance = super().__new__(cls)
        return cls._instance

s1 = Singleton()
s2 = Singleton()
print(s1 is s2)     # True —— 单例，永远只有一个实例

# ============================================================
# 10. 快速记忆总结
# ============================================================
# 用 is 的场景：None、True/False、单例、类型精确比较、枚举成员
# 用 == 的场景：数字、字符串的值、列表、字典、自定义对象的值的比较
